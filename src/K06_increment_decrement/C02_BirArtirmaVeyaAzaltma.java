package K06_increment_decrement;

public class C02_BirArtirmaVeyaAzaltma {

    public static void main(String[] args) {

        int a = 20;
        int b = 40;

        a++;

        System.out.println("12. satirda a'nin degeri : " + a ); //21

        ++a;

        System.out.println("16. satirda a'nin degeri :" + a); //22

        b--;

        System.out.println("20. satirda b'nin degeri : " +b); //39

        --b;

        System.out.println("24. satirda b'nin degeri : " + b); //38
    }
}
