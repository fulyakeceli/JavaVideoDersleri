package K06_increment_decrement;

public class C01_Increment_Decrement {

    public static void main(String[] args) {


        int sayi = 20;

        // sayinin degerini 2 katina cikarin

      //  sayi = sayi * 2;
        sayi *= 2;
        System.out.println("14. satirda variable'in degeri:" + sayi); //40


        // sayinin degerini 5 azaltin

       // sayi = sayi -5;
        sayi -= 5;
        System.out.println("21. satirda sayi variable'inin degeri:" + sayi); //35

        //sayiyi 5'e bolup sonucu sayi variable'ina atayin

        sayi /= 5;
        System.out.println("26. satirda dayi variable'inin degeri :" + sayi); //7
    }
}
